
public class Person {
	private int age;
	private String name;
	private int grade;
	
	void setAge(int years){
		age= years;
	}
	
	void setName(String letters) {
		name=letters;
	}
	
	void setGrade(int standard){
		grade= standard;
	}
	
	int getAge(){
		return age;
	}
	String getName(){
		return name;
	}
	int getGrade(){
		return grade;
	}
}


