public class TICalculator implements Calculator {

	public float add(float f1, float f2) {
		return f1+f2;
	}

	public float multiply(float f1, float f2) {
		return f1*f2;
	}

	public float divide(float f1, float f2) {
		return f1/f2;
	}

	public float subtract(float f1, float f2) {
		return f1-f2;
	}
	
}
